package org.curioswitch.common.server.framework.grpc;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;
import javax.inject.Qualifier;

/**
 * {@link Qualifier} to use to annotate the request to a gRPC producer graph when using {@link
 * dagger.BindsInstance}. It is almost always a good idea to add this annotation and create a
 * producer method that returns the unqualified request after validation.
 *
 * <p>This qualifier is not used by the framework and is only a convenience for user code where this
 * is almost always needed in many places.
 */
@Qualifier
@Retention(RetentionPolicy.CLASS)
@Target({ElementType.PARAMETER})
public @interface Unvalidated {}
